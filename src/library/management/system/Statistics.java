    private void issueBookShow(){
        try {
            conn con = new conn();
            String sql = "SELECT book_id as 'Book ID', student_id as 'Student ID', bname as 'Book Name', fname as 'First Name', lname as 'Last Name', course as 'Course', contact_info as 'Contact Info', dateOfIssue as 'Date Issued', dateOfExpectedReturn as 'Return Date', status as 'Status' FROM issueBook";
            PreparedStatement st = con.c.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            
            issueBookDetails.setModel(DbUtils.resultSetToTableModel(rs));
            rs.close();
            st.close();
            con.closeConnection();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

