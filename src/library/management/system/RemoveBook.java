    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        try{
            conn con = new conn();
            String sql = "select * from book where book_id=?";
            PreparedStatement st = con.c.prepareStatement(sql);

            st.setString(1, bookIdTF.getText());
            ResultSet rs = st.executeQuery();

            if (rs.next()){
                nameTF.setText(rs.getString("name"));
                isbnTF.setText(rs.getString("isbn"));
                publisherTF.setText(rs.getString("publisher"));
                editionTF.setText(rs.getString("edition"));
                priceTF.setText(rs.getString("price"));
                pagesTF.setText(rs.getString("pages"));
            } else {
                JOptionPane.showMessageDialog(null, "No information on Book ID given",  "Error!", JOptionPane.PLAIN_MESSAGE);
                bookIdTF.setText("");
            }
            con.closeConnection();
        }catch (Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_searchActionPerformed

