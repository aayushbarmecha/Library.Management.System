    private void trash(String isbnInfo, String nameInfo, String publisherInfo, String editionInfo, String priceInfo, String pagesInfo){
        try {
                conn con = new conn();
                String sql = "insert into deleted_book(isbn, name, publisher, edition, price, pages) values(?, ?, ?, ?, ?, ?)";
                PreparedStatement st = con.c.prepareStatement(sql);

                st.setString(1, isbnInfo);
                st.setString(2, nameInfo);
                st.setString(3, publisherInfo);
                st.setString(4, editionInfo);
                st.setString(5, priceInfo);
                st.setString(6, pagesInfo);
                
                int i = st.executeUpdate();
                con.closeConnection();
            }catch (Exception e){
                System.out.println(e);
            }
    }
    
    private void copyInfo(){
        if (isConfirmed == true){
            try {
                conn con = new conn();
                String sql = "select * from book where book_id=?";
                PreparedStatement st = con.c.prepareStatement(sql);
            
                st.setString(1, bookIdTF.getText());
                ResultSet rs = st.executeQuery();
            
                if (rs.next()){
                    String isbnInfo = rs.getString("isbn");
                    String nameInfo = rs.getString("name");
                    String publisherInfo = rs.getString("publisher");
                    String editionInfo = rs.getString("edition");
                    String priceInfo = rs.getString("price");
                    String pagesInfo = rs.getString("pages");
                    
                    con.closeConnection();
                    trash(isbnInfo, nameInfo, publisherInfo, editionInfo, priceInfo, pagesInfo);
                }
            } catch (Exception e) {
            
            }
        }
    }
    
    private void delete(){
        if (isConfirmed == true){
            try {
                conn con = new conn();
                String sql = "DELETE FROM book WHERE book_id  = ?";
                PreparedStatement st = con.c.prepareStatement(sql);
                
                String idString = bookIdTF.getText();
                int id = Integer.parseInt(idString);
                
                st.setInt(1, id);
                st.executeUpdate();
                JOptionPane.showMessageDialog(null,"Book successfully deleted from the database", "Success!", JOptionPane.PLAIN_MESSAGE);
                CheckUser check = new CheckUser();
                String user = check.checkUser();
                Logs write = new Logs();
                write.log("Librarian "+user+" removed Book ID "+bookIdTF.getText()+" information from the database");
                con.closeConnection();
            }catch (Exception e){
                System.out.println(e);
            }
            isConfirmed = false;
            bookIdTF.setText("");
            nameTF.setText("");
            isbnTF.setText("");
            publisherTF.setText("");
            editionTF.setText("");
            priceTF.setText("");
            pagesTF.setText("");
        }
    }
    
