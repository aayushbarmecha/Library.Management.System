    public void confirmation(){
        String confirmation = JOptionPane.showInputDialog(null,"Type CONFIRM to modify student information", "Confirm", JOptionPane.PLAIN_MESSAGE).toUpperCase();
        if (confirmation.equals("CONFIRM")){
            isConfirmed = true;
        }
    }
    
    public void passInfo(String studentID){
        StudentID = studentID;
        this.setVisible(true);
        try {
            conn con = new conn();
            
            String sql = "select * from student where student_id=?";
            PreparedStatement st = con.c.prepareStatement(sql);

            st.setString(1, StudentID);
            ResultSet rs = st.executeQuery();

            if (rs.next()){
                studentIdTF.setText(StudentID);
                fnameTF.setText(rs.getString("fname"));
                lnameTF.setText(rs.getString("lname"));
                courseTF.setSelectedItem(rs.getString("course"));
                contactInfoTF.setText(rs.getString("contact_info"));
            }
            con.closeConnection();
        } catch (Exception e) {
            
        }
    }
    
    private void saveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveChangesActionPerformed
        confirmation();
        if (isConfirmed == true){
            try {
                conn con = new conn();
                String sql = "UPDATE student SET fname=?, lname=?, course=?, contact_info=? WHERE student_id="+StudentID;
                PreparedStatement st = con.c.prepareStatement(sql);
                st.setString(1, fnameTF.getText());
                st.setString(2, lnameTF.getText());
                st.setString(3, (String)courseTF.getSelectedItem());
                st.setString(4, contactInfoTF.getText());
                st.executeUpdate();
                JOptionPane.showMessageDialog(null,"Student information successfully modified", "Success!", JOptionPane.PLAIN_MESSAGE);
                CheckUser check = new CheckUser();
                String user = check.checkUser();
                Logs write = new Logs();
                write.log("Librarian "+user+" modified Student Number "+StudentID+" information from the database");
                con.closeConnection();
            }catch (Exception e){
                System.out.println(e);
            }
            isConfirmed = false;
            this.setVisible(false);
            StudentDetails studentdetails = new StudentDetails();
            studentdetails.setVisible(true);
        }
    }//GEN-LAST:event_saveChangesActionPerformed

