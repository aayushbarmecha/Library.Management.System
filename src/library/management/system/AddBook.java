    private void checkInfo(){
        if (nameTF.getText().trim().isEmpty() || publisherTF.getText().trim().isEmpty() || editionTF.getText().trim().isEmpty() || pagesTF.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"No information should be empty", "Error!", JOptionPane.PLAIN_MESSAGE);
        } else {
            isComplete = true;
            checkUnique();
        }
    }
    
    private void checkUnique(){
        try{
            conn con = new conn();
            String sql = "select * from book where isbn=?";
            PreparedStatement st = con.c.prepareStatement(sql);
            st.setString(1, isbnTF.getText());
            ResultSet rs = st.executeQuery();
            if (rs.next()){
                JOptionPane.showMessageDialog(null,"ISBN already registered", "Error!", JOptionPane.PLAIN_MESSAGE);
            } else {
                isUnique = true;
            }
            con.closeConnection();
        } catch (Exception e){
        
        }
    }
    
    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        checkInfo();
        if (isComplete == true && isUnique == true){
            try {
                conn con = new conn();
                String sql = "insert into book(book_id, name, isbn, publisher, edition, price, pages) values(?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement st = con.c.prepareStatement(sql);

                st.setString(1, null);
                st.setString(2, nameTF.getText());
                st.setString(3, isbnTF.getText());
                st.setString(4, publisherTF.getText());
                st.setString(5, editionTF.getText());
                st.setString(6, priceTF.getText());
                st.setString(7, pagesTF.getText());

                int i = st.executeUpdate();

                if (i > 0){
                    ResultSet res = st.getGeneratedKeys();
                    JOptionPane.showMessageDialog(null,"Book successfully added into database", "Success!", JOptionPane.PLAIN_MESSAGE);
                    while (res.next()){
                        JOptionPane.showMessageDialog(null,"Book ID is "+res.getString(1), "Success!", JOptionPane.PLAIN_MESSAGE);
                    }
                }
                con.closeConnection();
            }catch (Exception e){
                    System.out.println(e);
            }
            isComplete = false;
            isUnique = false;

            CheckUser check = new CheckUser();
            String user = check.checkUser();
            Logs write = new Logs();
            write.log("Librarian "+user+" added "+nameTF.getText()+" to Book Database");

            nameTF.setText("");
            isbnTF.setText("");
            publisherTF.setText("");
            editionTF.setText("");
            priceTF.setText("");
            pagesTF.setText("");
        }
    }//GEN-LAST:event_addActionPerformed

