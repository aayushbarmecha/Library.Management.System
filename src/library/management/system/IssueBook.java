    private void studentSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentSearchActionPerformed
        try{
            conn con = new conn();
            String sql = "select * from student where student_id=?";
            PreparedStatement st = con.c.prepareStatement(sql);

            st.setString(1, studentIdTF.getText());
            ResultSet rs = st.executeQuery();

            if (rs.next()){
                fnameTF.setText(rs.getString("fname"));
                lnameTF.setText(rs.getString("lname"));
                courseTF.setText(rs.getString("course"));
                contactInfoTF.setText(rs.getString("contact_info"));
            } else {
                JOptionPane.showMessageDialog(null, "No information on Student ID given",  "Error!", JOptionPane.PLAIN_MESSAGE);
                studentIdTF.setText("");
                fnameTF.setText("");
                lnameTF.setText("");
                courseTF.setText("");
                contactInfoTF.setText("");
            }
            con.closeConnection();
        }catch (Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_studentSearchActionPerformed

