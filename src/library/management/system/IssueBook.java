    private void checkInfo(){
        String date  = ((JTextField)dateOfIssueTF.getDateEditor().getUiComponent()).getText();
        if (nameTF.getText().trim().isEmpty() || fnameTF.getText().trim().isEmpty() || lnameTF.getText().trim().isEmpty() || courseTF.getText().trim().isEmpty() || contactInfoTF.getText().trim().isEmpty() || date.trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Specify book and student details", "Error!", JOptionPane.PLAIN_MESSAGE);
        } else {
            isComplete = true;
        }
    }
    
