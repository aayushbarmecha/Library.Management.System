    private void issueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueActionPerformed
        checkInfo();
        if (isComplete == true){
            String issueDate  = ((JTextField)dateOfIssueTF.getDateEditor().getUiComponent()).getText();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("MMM d, y");
            LocalDate issue = LocalDate.parse(issueDate, formatter);
            LocalDate expected = issue.plusDays(30);
            String expectedDateOfReturn = expected.format(formatter);

            try {
                conn con = new conn();
                String sql = "insert into issueBook(book_id, student_id, bname, fname, lname, course, contact_info, dateOfIssue, dateOfExpectedReturn) values(?, ?, ?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement st = con.c.prepareStatement(sql);

                st.setString(1, bookIdTF.getText());
                st.setString(2, studentIdTF.getText());
                st.setString(3, nameTF.getText());
                st.setString(4, fnameTF.getText());
                st.setString(5, lnameTF.getText());
                st.setString(6, courseTF.getText());
                st.setString(7, contactInfoTF.getText());
                st.setString(8, issueDate);
                st.setString(9, expectedDateOfReturn);

                int i = st.executeUpdate();

                if (i > 0){
                    JOptionPane.showMessageDialog(null,"Book successfully issued", "Success!", JOptionPane.PLAIN_MESSAGE);
                    JOptionPane.showMessageDialog(null,"Student can return book until "+expectedDateOfReturn, "Success!", JOptionPane.PLAIN_MESSAGE);
                }
                CheckUser check = new CheckUser();
                String user = check.checkUser();
                Logs write = new Logs();
                write.log("Librarian "+user+" issued book number "+bookIdTF.getText()+" to student number "+studentIdTF.getText()+" on "+issueDate);

                con.closeConnection();
                this.setVisible(false);
                main menu = new main();
                menu.setVisible(true);
            }catch (Exception e){
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_issueActionPerformed

